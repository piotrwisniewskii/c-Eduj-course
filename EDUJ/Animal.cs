using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace EDUJ
{
    public class Animal : Object // DZIEDZICZENIE NIEJAWNE
    {
       public string name;
        public int age;
        public void Show()
        {
            Console.Write($"Imie: {name}, wiek: {age}");
        }
        public Animal(string name, int age)
        {
            this.name = name;
            this.age = age;
        }
    }
    public class Cat : Animal
    {
        public string color;
        public void Show()
        {
            base.Show();
            Console.WriteLine($"kolor {color}");
        }

        public Cat(string name, int age, string color) : base(name,age)
        {
            this.color = color;
        }
    }
} 
